public class equation {
    public static void main(String[] args) {
        int num1 = 20;
        int num2 = 10;

        int add = num1 + num2;
        int min = num1 - num2;
        int mult = num1 * num2;
        int div = num1 / num2;

        System.out.println(num1 + " + " + num2 + " = " + add);
        System.out.println(num1 + " - " + num2 + " = " + min);
        System.out.println(num1 + " * " + num2 + " = " + mult);
        System.out.println(num1 + " / " + num2 + " = " + div);

    }
}
